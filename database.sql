CREATE DATABASE IF NOT EXISTS landings_db CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE landings_db;

CREATE TABLE IF NOT EXISTS landings (
    id INT AUTO_INCREMENT PRIMARY KEY,
    landing_name VARCHAR(255) NOT NULL UNIQUE,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE IF NOT EXISTS landings_rk (
    id INT AUTO_INCREMENT PRIMARY KEY,
    landing_id INT NOT NULL,
    rk_name VARCHAR(255) NOT NULL,
    landing_motive TEXT NULL,
    title TEXT NULL,
    lid TEXT NULL,
    button_text VARCHAR(255) DEFAULT '',
    oz_title VARCHAR(255) DEFAULT '',
    mag_comment TEXT NULL,
    img_path VARCHAR(255) DEFAULT '',

    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,

    CONSTRAINT fk_landing FOREIGN KEY (landing_id) REFERENCES landings(id) ON DELETE CASCADE,
    UNIQUE KEY unique_landing_rk (landing_id, rk_name)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

